      The Earth’s marine systems provide multiple ecosystem goods and services essential for human well-being and socio-economic development. Undeniable trends, however, indicate that the health and productivity of these systems are being degraded by multiple and complex anthropogenic and natural stressors.<br/>
      The GEF-funded Transboundary Waters Assessment Programme (<a href="http://www.geftwap.org/">TWAP</a>) is assessing transboundary aquifers, reservoirs and lakes, river basins, Large Marine Ecosystems (LMEs) and the open ocean to guide interventions to improve the environment and provide benefits for human well-being.<br/>
      The TWAP marine assessments were led by the Intergovernmental Oceanographic Commission of UNESCO:
      <ul style="padding-top:0; margin-top:0; margin-bottom:0; padding-bottom:0">
        <li>The <a href="/open_ocean">Open Ocean</a> is the largest of the planet’s transboundary water spaces and is the medium for many global issues with local impacts</li>
        <li>The 66 <a href="/lmes">Large Marine Ecosystems</a> encompass the Earth’s coastal areas where many ecosystem services and human stressors are concentrated</li>
      </ul>
